#include <stdio.h>
#include <stdlib.h> // malloc, free 쓰려면 이거 필요함

// [이론 정리 1탄: 포인터와 배열, 그리고 동적 할당]
// C언어의 꽃이자 최대 고비인 포인터 관련해서 싹 정리해봄.

int main() {
    // ==========================================
    // 1. 포인터 기초
    // ==========================================
    int a = 10;
    int* ptr = &a; // 포인터 변수 ptr에 a의 '주소'를 쓱싹 집어넣음.

    // *ptr은 그 주소로 '찾아가서' 값을 가져오거나 바꾸겠다는 뜻임. (역참조)
    *ptr = 20; 
    printf("포인터로 바꾼 a의 값: %d\n", a); // 20 출력됨. 변수 a를 직접 안 건드렸는데 값이 바뀜! 신기방기.

    // ==========================================
    // 2. 배열과 포인터의 소름돋는 관계
    // ==========================================
    // 배열 이름은 사실상 '배열의 첫 번째 요소 주소'를 의미하는 상수임. (포인터 상수)
    int arr[3] = {100, 200, 300};
    int* pArr = arr; // arr 자체가 &arr[0]이랑 완전 같음.

    // 인덱스로 접근하나 포인터 연산으로 접근하나 결과는 똑같음.
    printf("arr[1]: %d, *(pArr + 1): %d\n", arr[1], *(pArr + 1)); 
    
    // [핵심] 포인터에 +1 하면 1바이트 더하는게 아님! 
    // 자료형 크기(int면 4바이트)만큼 껑충 점프함! 이거 주소 계산할 때 개중요함.

    // ==========================================
    // 3. 포인터 배열 vs 배열 포인터 (이름 헷갈림 주의)
    // ==========================================
    // - 포인터 배열: 포인터들을 여러 개 모아둔 배열임. (문자열 배열 만들 때 개꿀)
    const char* strArr[2] = {"Hello", "World"}; // 각각 문자열 상수의 주소를 저장함.
    printf("첫번째 문자열: %s\n", strArr[0]);

    // ==========================================
    // 4. 동적 할당 (Dynamic Allocation - chap_14)
    // ==========================================
    // 메모리가 얼마나 필요할지 컴파일할 때 모를 때, 런타임에 필요한 만큼만 힙(Heap)에서 빌려쓰는 거임. 
    // 다 쓰면 반납(free) 필수! 안그러면 메모리 누수(Memory Leak) 나서 컴터 터짐.
    
    // int 크기 3개(12바이트) 빌림. 리턴값이 void* 라서 (int*)로 형변환 해줘야 됨.
    int* dPtr = (int*)malloc(sizeof(int) * 3); 
    
    if (dPtr != NULL) { // 메모리 할당 실패했는지 체크 (국룰)
        dPtr[0] = 10; dPtr[1] = 20; dPtr[2] = 30; // 그냥 배열처럼 쓰면 됨. 편안.
        printf("동적 할당된 두번째 값: %d\n", dPtr[1]);
        
        free(dPtr); // 다 썼으니까 반납함. 이거 까먹으면 진짜 큰일남.
    }

    return 0;
}
