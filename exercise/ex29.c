/*
구구단 출력 (2~9)
*/
#include <stdio.h>

int main()
{
    int i, j; // i는 '단', j는 '곱해지는 수'

    // 바깥쪽 반복문: 2단부터 9단까지 반복
    for (i = 2; i <= 9; i++)
    {
        printf("=== %d단 ===\n", i); // 단의 제목 출력

        // 안쪽 반복문: 1부터 9까지 곱하기
        for (j = 1; j <= 9; j++)
        {
            // %d * %d = %d 형식으로 출력
            printf("%d * %d = %d\n", i, j, i * j);
        }

        printf("\n"); // 단이 끝날 때마다 줄바꿈을 해서 보기 좋게 만듦
    }

    return 0;
}