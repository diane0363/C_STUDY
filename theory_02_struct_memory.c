#include <stdio.h>
#include <stdlib.h>
#include <string.h> // strcpy 쓰려고 가져옴

// [이론 정리 2탄: 구조체와 메모리 구조]
// 관련 있는 데이터를 하나로 묶어서 관리할 때 구조체만한게 없음. 메모리 구조랑 같이 복습해봄.

// ==========================================
// 1. 구조체 (Struct) 정의 (chap_16)
// ==========================================
// 서로 다른 자료형들을 짬뽕해서 나만의 새로운 타입을 만드는 거임.
struct Student {
    char name[20];
    int age;
    double score;
}; // <- 구조체 끝에 세미콜론(;) 꼭 붙여야 함! (은근 많이 빼먹고 에러나서 화냄)

// typedef 쓰면 struct 어쩌구 매번 길게 안써도 돼서 개편함. 
// 밑에 Point는 이제 그냥 int나 double 같은 하나의 타입 이름이 된거임.
typedef struct {
    int x;
    int y;
} Point;

int main() {
    // ==========================================
    // 2. 구조체 변수 만들고 써보기
    // ==========================================
    struct Student s1 = {"홍길동", 20, 95.5}; // 초기화 할 때 중괄호 씀
    
    // 점(.) 찍고 멤버(안에 든 변수들)에 접근하면 됨.
    printf("이름: %s, 나이: %d\n", s1.name, s1.age);

    // [주의] 문자열(배열)은 대입연산자(=)로 통째로 덮어쓰기 안됨! 
    // s1.name = "김철수"; // <- 이렇게 하면 바로 에러남 ㅠㅠ
    strcpy(s1.name, "김철수"); // 그래서 string.h 인클루드 하고 strcpy 써야됨.

    // typedef로 만든건 진짜 편함. struct 안 써도 됨. 개깔끔.
    Point p1 = {10, 20};

    // ==========================================
    // 3. 구조체 포인터와 화살표(->) 연산자
    // ==========================================
    struct Student* pStu = &s1; // 구조체도 당연히 포인터에 주소 담을 수 있음.

    // 포인터로 멤버 접근할 때는 (*pStu).age 이렇게 써도 되지만... 괄호 치기 귀찮음.
    // 그래서 화살표(->) 연산자가 등장함! 실무나 자료구조(LinkedList 등)에서 밥먹듯이 쓰임.
    printf("포인터로 접근한 나이: %d\n", pStu->age); 

    // ==========================================
    // 4. 메모리 영역 4총사 복습 (chap_14, 15)
    // ==========================================
    // - 코드(Code) 영역 : 내가 짠 코드(명령어)가 올라감. 수정 불가.
    // - 데이터(Data) 영역 : 전역 변수, static 변수 들어감. 프로그램 시작부터 끝까지 살아있음.
    // - 힙(Heap) 영역 : malloc()으로 빌려쓰고 free()로 갚는 동네. 내 맘대로 조절 가능. 동적 할당의 무대임.
    // - 스택(Stack) 영역 : 지역 변수, 매개변수 들어감. 함수 중괄호 {} 끝나면 얄짤없이 다 날아감. LIFO 구조.

    // [응용] 힙 영역에 구조체 동적 할당해보기
    Point* pPtr = (Point*)malloc(sizeof(Point));
    if (pPtr != NULL) {
        pPtr->x = 100; // 포인터니까 당연히 점(.) 대신 화살표(->) 씀
        pPtr->y = 200;
        
        printf("동적 할당된 점: (%d, %d)\n", pPtr->x, pPtr->y);
        free(pPtr); // 메모리 빚진거 잊지말고 반납!
    }

    return 0;
}
